package estruturaRepeticao;

public class EstruturaRepeticao {

	public static void main(String[] args) {

		// for

		for (int i = 0; i < 10; i++) {
			for (int j = 0; j < 10; j++)
				System.out.print("*");
			System.out.println();

		}

		for (int i = 0; i < 8; i++) {
			
			for (int j = 1; j < i; j++)
			
			System.out.print(j);
			System.out.println();

		}
		
		System.out.println();
		
		for (int i = 7; i > 1; i--) {
			
			for (int j = 1; j < i; j++)
			
			System.out.print(j);
			System.out.println();

		}
		

		// while

		// do while

	}
}
